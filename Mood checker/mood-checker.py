mood = input("Hello! Please enter your mood: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Have some chocolate :)")
elif mood == "excited":
    print("We are excited for you!")
elif mood == "relaxed":
    print("Great, stay relaxed.")
else:
    print("I don't recognize this mood.")
